// Temperature Converter Farenheit Celsius

let farenheit = 50;

// todo Calculate celsius and store in celsius variable
let celsius = (farenheit - 32) * 5 / 9;
// print that value 
console.log(`Temperature in Farenheit is ${farenheit} F = ${celsius} C`)

// Calculate kelvin value and store it in a variable
let kelvin = (farenheit + 459.67) * 5 / 9;
// print that value
console.log(`Temperature in Farenheit is ${farenheit} F = ${kelvin} C`)